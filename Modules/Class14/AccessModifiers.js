/**
 * Access Modifiers
 * 
 *      defines from where we can access the variables/functions of a class
 * 
 * public - variable/function can be accessed from anywhere in the project
 *          In js, everything is public by default
 * private - variable/function CANNOT be accessed from outside the class
 *          In js, prefix variable/function name with #
 * protected - variable/function can be access only within the package
 *
 */